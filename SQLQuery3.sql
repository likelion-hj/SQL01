

CREATE TABLE EMPLOYEE (
  eno      varchar(15), 
  ename    VARCHAR(30),
  zip      int DEFAULT '47405',
  hdate    DATE,
  dept 	   VARCHAR(30),
  salary   decimal(15,3),
  constraint EMPLOYEE_PK primary key (eno));
  
  
CREATE TABLE PART(
  pno      varchar(8),
  pname    VARCHAR(50),
  price    decimal(6,2) CHECK(PRICE >= 0.0),
 discount_percent decimal(5, 2),
  olevel   int DEFAULT '20',
  constraint PART_PK primary key(pno));

CREATE TABLE CUSTOMER (
  cno      varchar(15),
  cname    VARCHAR(30),
  street   VARCHAR(30),
  zip      varchar(11),
  phone    CHAR(12),
  constraint CUSTOMER_PK primary key (cno));

CREATE TABLE ORDER1 (
  ono      varchar(15),
  cno      varchar(15),
  eno      varchar(15),
  received DATE,
  shipped  DATE,
  constraint ORDER1_PK primary key (ono),
  constraint ORDER1_FK1 foreign key (cno) references CUSTOMER(cno),
  constraint ORDER1_FK2 foreign key (eno) references EMPLOYEE(eno));
  
CREATE TABLE ODETAILS (
  ONO      varchar(15),
  PNO	   varchar(8),
  QTY      int CHECK(QTY > 0),
  constraint ODETAILS_PK PRIMARY KEY (ono,pno),
  constraint ODETAILS_FK1 foreign key (ono) references ORDER1(ono),
  constraint ODETAILS_FK2 foreign key (pno) references PART(pno));

  
